import { Schema, model } from "mongoose";
import { PostModel } from "../@types/types";

const postSchema = new Schema<PostModel>({
  title: String,
  message: String,
  creator: String,
  tags: [String],
  selectedFile: String,
  likeCount: {
    type: Number,
    default: 0,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

export default model<PostModel>("post", postSchema);
